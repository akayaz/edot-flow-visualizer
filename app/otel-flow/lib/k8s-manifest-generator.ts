function generateService(data: K8sDeploymentNodeData): string {
  return `apiVersion: v1
kind: Service
metadata:
  name: ${data.name}
  namespace: ${data.namespace}
  labels:
    app: edot-collector
    component: gateway
spec:
  type: ClusterIP
  selector:
    app: edot-collector
    component: gateway
  ports:
  - name: otlp-grpc
    port: 4317
    targetPort: 4317
    protocol: TCP
  - name: otlp-http
    port: 4318
    targetPort: 4318
    protocol: TCP
  - name: metrics
    port: 8888
    targetPort: 8888
    protocol: TCP`;
}

function generateConfigMap(namespace: string): string {
  return `apiVersion: v1
kind: ConfigMap
metadata:
  name: edot-collector-config
  namespace: ${namespace}
data:
  collector.yaml: |
    receivers:
      otlp:
        protocols:
          grpc:
            endpoint: 0.0.0.0:4317
          http:
            endpoint: 0.0.0.0:4318
      hostmetrics:
        collection_interval: 30s
        scrapers:
          cpu:
          memory:
          disk:
          network:

    processors:
      memory_limiter:
        limit_mib: 512
        spike_limit_mib: 128
        check_interval: 5s
      batch:
        timeout: 10s
        send_batch_size: 1024

    exporters:
      elasticsearch:
        endpoint: \${ELASTIC_APM_SERVER_URL}
        headers:
          Authorization: "Bearer \${ELASTIC_APM_SECRET_TOKEN}"

    service:
      pipelines:
        traces:
          receivers: [otlp]
          processors: [memory_limiter, batch]
          exporters: [elasticsearch]
        metrics:
          receivers: [otlp, hostmetrics]
          processors: [memory_limiter, batch]
          exporters: [elasticsearch]
        logs:
          receivers: [otlp]
          processors: [memory_limiter, batch]
          exporters: [elasticsearch]
---
apiVersion: v1
kind: ServiceAccount
metadata:
  name: edot-collector
  namespace: ${namespace}
---
apiVersion: rbac.authorization.k8s.io/v1
kind: ClusterRole
metadata:
  name: edot-collector
rules:
- apiGroups: [""]
  resources: ["pods", "nodes", "services", "namespaces", "endpoints"]
  verbs: ["get", "list", "watch"]
- apiGroups: ["apps"]
  resources: ["replicasets", "deployments", "daemonsets", "statefulsets"]
  verbs: ["get", "list", "watch"]
- apiGroups: ["batch"]
  resources: ["jobs", "cronjobs"]
  verbs: ["get", "list", "watch"]
---
apiVersion: rbac.authorization.k8s.io/v1
kind: ClusterRoleBinding
metadata:
  name: edot-collector
roleRef:
  apiGroup: rbac.authorization.k8s.io
  kind: ClusterRole
  name: edot-collector
subjects:
- kind: ServiceAccount
  name: edot-collector
  namespace: ${namespace}`;
}

/**
 * Downloads the k8s-manifests.yaml file
 */
export function downloadK8sManifests(content: string) {
  const blob = new Blob([content], { type: 'text/yaml' });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = 'k8s-manifests.yaml';
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
  URL.revokeObjectURL(url);
}
