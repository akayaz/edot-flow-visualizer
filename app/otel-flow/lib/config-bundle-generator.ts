/**
 * Convert label to file-safe name
 */
function toFileName(label: string): string {
  return label
    .toLowerCase()
    .replace(/[^a-z0-9-_]/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '') || 'config';
}

/**
 * Get EDOT SDK name for a language
 */
function getEdotSdkName(language: string): string {
  switch (language) {
    case 'nodejs': return EDOT_VERSIONS.nodePackage;
    case 'python': return EDOT_VERSIONS.pythonPackage;
    case 'java': return `elastic-otel-javaagent:${EDOT_VERSIONS.javaAgent}`;
    case 'dotnet': return EDOT_VERSIONS.dotnetPackage;
    default: return 'upstream OTel SDK';
  }
}

/**
 * Generate all bundle files
 */
function generateBundleFiles(
  nodes: Node<EDOTNodeData>[],
  edges: Edge<FlowEdgeData>[],
  deploymentModel: DeploymentModel
): BundleFile[] {
  const files: BundleFile[] = [];

  // Get collector nodes
  const collectorNodes = nodes.filter(
    (n) => n.data.componentType === 'collector-agent' || n.data.componentType === 'collector-gateway'
  ) as Node<CollectorNodeData>[];

  // Get SDK nodes
  const sdkNodes = nodes.filter(
    (n) => n.data.componentType === 'edot-sdk'
  ) as Node<SDKNodeData>[];

  // Generate collector configs
  for (const collector of collectorNodes) {
    const fileName = toFileName(collector.data.label);
    const yaml = generateCollectorYAML(collector.data, { deploymentModel });
    files.push({
      path: `configs/${fileName}-config.yaml`,
      content: yaml,
    });
  }

  // Generate sample app source code and Dockerfiles for each SDK
  for (const sdk of sdkNodes) {
    const appName = toFileName(sdk.data.serviceName || sdk.data.label);
    
    // Generate Dockerfile
    const dockerfile = generateDockerfile(sdk.data.language, sdk.data.serviceName || sdk.data.label);
    files.push({
      path: `apps/${appName}/Dockerfile`,
      content: dockerfile,
    });

    // Generate sample app source files
    const appFiles = generateSampleApp(sdk.data);
    for (const [filename, content] of appFiles) {
      files.push({
        path: `apps/${appName}/${filename}`,
        content,
      });
    }
  }

  // Check for infrastructure nodes
  const hasDocker = nodes.some((n) => n.type === 'infrastructureDocker') || 
                    nodes.some((n) => n.data.componentType === 'edot-sdk' || 
                                       n.data.componentType === 'collector-agent' ||
                                       n.data.componentType === 'collector-gateway');
  const hasK8s = nodes.some(
    (n) =>
      n.type === 'infrastructureK8sNamespace' ||
      n.type === 'infrastructureK8sDaemonSet' ||
      n.type === 'infrastructureK8sDeployment'
  ) || (collectorNodes.length > 0 || sdkNodes.length > 0);

  // Generate Docker Compose
  if (hasDocker) {
    const dockerCompose = generateDockerCompose(nodes, edges, deploymentModel);
    files.push({
      path: 'docker-compose.yml',
      content: dockerCompose,
    });
  }

  // Generate K8s manifests
  if (hasK8s) {
    const k8sManifests = generateK8sManifests(nodes, edges, { deploymentModel });
    files.push({
      path: 'kubernetes/manifests.yaml',
      content: k8sManifests,
    });
  }

  // Generate README
  const readme = generateReadme(nodes, deploymentModel, hasK8s, hasDocker);
  files.push({
    path: 'README.md',
    content: readme,
  });

  // Generate .env.example
  const envExample = generateEnvExample(deploymentModel);
  files.push({
    path: '.env.example',
    content: envExample,
  });

  // Generate Makefile
  if (hasDocker) {
    files.push({
      path: 'Makefile',
      content: generateMakefile(),
    });
  }

  // Generate architecture docs
  const archDocs = generateArchitectureDocs(nodes, edges, deploymentModel);
  files.push({
    path: 'docs/architecture.md',
    content: archDocs,
  });

  return files;
}

/**
 * Generate a ZIP bundle containing all configuration files
 */
export async function generateConfigBundle(
  nodes: Node<EDOTNodeData>[],
  edges: Edge<FlowEdgeData>[],
  deploymentModel: DeploymentModel
): Promise<Blob> {
  const zip = new JSZip();
  
  // Generate all files
  const files = generateBundleFiles(nodes, edges, deploymentModel);
  
  // Add files to ZIP
  for (const file of files) {
    zip.file(file.path, file.content);
  }
  
  // Generate ZIP blob
  return zip.generateAsync({ 
    type: 'blob',
    compression: 'DEFLATE',
    compressionOptions: { level: 6 },
  });
}

/**
 * Download the config bundle as a ZIP file
 */
export async function downloadConfigBundle(
  nodes: Node<EDOTNodeData>[],
  edges: Edge<FlowEdgeData>[],
  deploymentModel: DeploymentModel
): Promise<void> {
  const blob = await generateConfigBundle(nodes, edges, deploymentModel);
  
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = 'edot-config-bundle.zip';
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
  URL.revokeObjectURL(url);
}

/**
 * Get a preview of what files will be in the bundle
 */
export function previewBundleContents(
  nodes: Node<EDOTNodeData>[],
  edges: Edge<FlowEdgeData>[],
  deploymentModel: DeploymentModel
): { path: string; size: number }[] {
  const files = generateBundleFiles(nodes, edges, deploymentModel);
  return files.map((f) => ({
    path: f.path,
    size: new Blob([f.content]).size,
  }));
}
