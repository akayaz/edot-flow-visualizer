interface BundleFile {
  path: string;
  content: string;
}

interface ConfigBundle {
  files: BundleFile[];
  readme: string;
}

/**
 * Generate README.md content for the bundle
 */
function generateReadme(
  nodes: Node<EDOTNodeData>[],
  deploymentModel: DeploymentModel,
  hasK8s: boolean,
  hasDocker: boolean
): string {
  const collectorNodes = nodes.filter(
    (n) => n.data.componentType === 'collector-agent' || n.data.componentType === 'collector-gateway'
  );
  const sdkNodes = nodes.filter((n) => n.data.componentType === 'edot-sdk') as Node<SDKNodeData>[];
  
  const deploymentInfo = {
    serverless: {
      name: 'Elastic Cloud Serverless',
      endpoint: 'Your Serverless project OTLP endpoint',
    },
    ech: {
      name: 'Elastic Cloud Hosted',
      endpoint: 'Your ECH deployment OTLP endpoint',
    },
    'self-managed': {
      name: 'Self-Managed Elasticsearch',
      endpoint: 'Your Elasticsearch cluster endpoint',
    },
  }[deploymentModel];

  let readme = `# EDOT Configuration Bundle

Generated by EDOT Flow Visualizer

## Overview

This bundle contains a fully runnable project to deploy your OpenTelemetry infrastructure with Elastic's Distribution of OpenTelemetry (EDOT).

**Deployment Model:** ${deploymentInfo.name}
**EDOT Collector Image:** \`${EDOT_COLLECTOR_IMAGE}\`

### Architecture Summary

`;

  // Add architecture description
  if (sdkNodes.length > 0) {
    readme += `- **${sdkNodes.length} Application(s):** `;
    readme += sdkNodes.map((n) => `${n.data.serviceName || n.data.label} (${n.data.language})`).join(', ');
    readme += '\n';
  }
  
  const agents = collectorNodes.filter((n) => n.data.componentType === 'collector-agent');
  const gateways = collectorNodes.filter((n) => n.data.componentType === 'collector-gateway');
  
  if (agents.length > 0) {
    readme += `- **${agents.length} Collector Agent(s):** Per-host/sidecar telemetry collection\n`;
  }
  if (gateways.length > 0) {
    readme += `- **${gateways.length} Collector Gateway(s):** Centralized processing and routing\n`;
  }

  readme += `
## Bundle Contents

\`\`\`
`;

  if (sdkNodes.length > 0) {
    readme += `apps/\n`;
    for (const sdk of sdkNodes) {
      const name = toFileName(sdk.data.serviceName || sdk.data.label);
      readme += `  ├── ${name}/\n`;
      readme += `  │   ├── Dockerfile              # EDOT-instrumented container\n`;
      readme += `  │   └── (app source files)       # Sample ${sdk.data.language} application\n`;
    }
  }

  if (collectorNodes.length > 0) {
    readme += `configs/\n`;
    for (const collector of collectorNodes) {
      const fileName = toFileName(collector.data.label);
      readme += `  ├── ${fileName}-config.yaml    # ${collector.data.componentType === 'collector-gateway' ? 'Gateway' : 'Agent'} configuration\n`;
    }
  }

  if (hasDocker) {
    readme += `docker-compose.yml              # Docker Compose for local deployment\n`;
  }

  if (hasK8s) {
    readme += `kubernetes/\n`;
    readme += `  ├── manifests.yaml              # K8s DaemonSet/Deployment manifests\n`;
  }

  readme += `.env.example                      # Environment variables template
Makefile                          # Convenience targets (up, down, logs)
README.md                         # This file
docs/
  └── architecture.md             # Architecture documentation
\`\`\`

## Quick Start

### Prerequisites

1. Elastic Observability deployment (Serverless, Cloud Hosted, or Self-Managed)
2. Docker and Docker Compose (for local testing)
3. kubectl configured (for Kubernetes deployment)

### Step 1: Configure Environment Variables

Copy the example environment file and fill in your values:

\`\`\`bash
cp .env.example .env
\`\`\`

Edit \`.env\` with your Elastic credentials:

\`\`\`bash
# Required
ELASTIC_APM_SERVER_URL=https://your-deployment.apm.elastic-cloud.com:443
ELASTIC_APM_SECRET_TOKEN=your-secret-token

# Optional
DEPLOYMENT_ENVIRONMENT=production
\`\`\`

`;

  if (hasDocker) {
    readme += `### Step 2: Deploy with Docker Compose

\`\`\`bash
# Load environment variables
source .env

# Build and start all services
make up

# Or manually:
docker-compose up -d --build

# View logs
make logs

# Stop services
make down
\`\`\`

`;

    // Add per-app access info
    if (sdkNodes.length > 0) {
      readme += `### Application Endpoints

| Service | URL | Language | EDOT SDK |
|---------|-----|----------|----------|
`;
      let port = 8080;
      for (const sdk of sdkNodes) {
        const edotSdk = getEdotSdkName(sdk.data.language);
        readme += `| ${sdk.data.serviceName || sdk.data.label} | http://localhost:${port} | ${sdk.data.language} | ${edotSdk} |\n`;
        port++;
      }
      readme += '\n';
    }
  }

  if (hasK8s) {
    readme += `### Step 2: Deploy to Kubernetes

\`\`\`bash
# Create namespace
kubectl create namespace observability

# Create secret with credentials
kubectl create secret generic elastic-apm \\
  --from-literal=server-url="\${ELASTIC_APM_SERVER_URL}" \\
  --from-literal=secret-token="\${ELASTIC_APM_SECRET_TOKEN}" \\
  -n observability

# Apply manifests
kubectl apply -f kubernetes/manifests.yaml

# Verify deployment
kubectl get pods -n observability
\`\`\`

`;
  }

  readme += `## EDOT Components Used

This project uses **Elastic's Distribution of OpenTelemetry (EDOT)**, not upstream OpenTelemetry packages.

| Component | EDOT Package | Version |
|-----------|-------------|---------|
| Collector | \`${EDOT_COLLECTOR_IMAGE}\` | ${EDOT_VERSIONS.collector} |
`;

  if (sdkNodes.some((n) => n.data.language === 'nodejs')) {
    readme += `| Node.js SDK | \`${EDOT_VERSIONS.nodePackage}\` | latest |\n`;
  }
  if (sdkNodes.some((n) => n.data.language === 'python')) {
    readme += `| Python SDK | \`${EDOT_VERSIONS.pythonPackage}\` | latest |\n`;
  }
  if (sdkNodes.some((n) => n.data.language === 'java')) {
    readme += `| Java Agent | \`co.elastic.otel:elastic-otel-javaagent\` | ${EDOT_VERSIONS.javaAgent} |\n`;
  }
  if (sdkNodes.some((n) => n.data.language === 'dotnet')) {
    readme += `| .NET SDK | \`${EDOT_VERSIONS.dotnetPackage}\` | latest |\n`;
  }

  readme += `
## Documentation

- [EDOT Documentation](https://www.elastic.co/docs/reference/opentelemetry)
- [EDOT Collector Configuration](https://www.elastic.co/docs/reference/edot-collector)
- [OpenTelemetry Documentation](https://opentelemetry.io/docs/)

## Support

For issues and questions:
- [Elastic Discuss Forums](https://discuss.elastic.co/)
- [GitHub Issues](https://github.com/elastic/opentelemetry/issues)

---

*Generated by [EDOT Flow Visualizer](https://github.com/elastic/opentelemetry)*
`;

  return readme;
}

/**
 * Generate .env.example content
 */
function generateEnvExample(deploymentModel: DeploymentModel): string {
  let content = `# EDOT Configuration Environment Variables
# Copy this file to .env and fill in your values

# ============================================
# Elastic Observability Credentials
# ============================================

`;

  if (deploymentModel === 'self-managed') {
    content += `# Elasticsearch endpoint (self-managed)
ELASTICSEARCH_ENDPOINT=https://your-elasticsearch:9200
ELASTICSEARCH_API_KEY=your-api-key

# For local development with Docker
ELASTIC_PASSWORD=changeme
KIBANA_PASSWORD=changeme
`;
  } else {
    content += `# Elastic Cloud endpoint
ELASTIC_APM_SERVER_URL=https://your-deployment.apm.elastic-cloud.com:443
ELASTIC_APM_SECRET_TOKEN=your-secret-token
`;
  }

  content += `
# ============================================
# Collector Configuration
# ============================================

# Storage directory for persistent state
STORAGE_DIR=/var/lib/otelcol

# Deployment environment name
DEPLOYMENT_ENVIRONMENT=production

# Gateway endpoint (for agent -> gateway forwarding)
GATEWAY_ENDPOINT=gateway:4317

# ============================================
# Optional Settings
# ============================================

# Log level (debug, info, warn, error)
OTEL_LOG_LEVEL=info

# Memory limit for collectors (MiB)
MEMORY_LIMIT_MIB=512
`;

  return content;
}

/**
 * Generate Makefile with convenience targets
 */
function generateMakefile(): string {
  return `.PHONY: up down logs build clean restart status

# Start all services
up:
\tdocker-compose up -d --build

# Stop all services
down:
\tdocker-compose down

# View logs (follow mode)
logs:
\tdocker-compose logs -f

# Build all images without starting
build:
\tdocker-compose build

# Stop and remove all containers, networks, and volumes
clean:
\tdocker-compose down -v --remove-orphans

# Restart all services
restart:
\tdocker-compose down && docker-compose up -d --build

# Show service status
status:
\tdocker-compose ps

# Show service health
health:
\t@echo "Checking service health..."
\t@docker-compose ps --format "table {{.Name}}\\t{{.Status}}"
`;
}

